    private String obtenerMetadatos(File imagen) {
        StringBuilder metadatos = new StringBuilder();
        try {
            Metadata metadata = ImageMetadataReader.readMetadata(imagen);
            for (Directory directory : metadata.getDirectories()) {
                for (Tag tag : directory.getTags()) {
                    metadatos.append(tag.getTagName()).append(": ").append(tag.getDescription()).append("\n");
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            return "Error al obtener metadatos";
        }
        return metadatos.toString();
    }
    
    private String encriptarSHA256(String datos) {
        try {
            MessageDigest digest = MessageDigest.getInstance("SHA-256");
            byte[] hash = digest.digest(datos.getBytes("UTF-8"));
            StringBuilder hexString = new StringBuilder();
            for (byte b : hash) {
                String hex = Integer.toHexString(0xff & b);
                if (hex.length() == 1) hexString.append('0');
                hexString.append(hex);
            }
            return hexString.toString();
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }
    
