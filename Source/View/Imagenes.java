    private void BgenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BgenerarActionPerformed
        try {
        File imagen1 = new File(rutaImagen1);
        File imagen2 = new File(rutaImagen2);

        
        String metadatos1 = obtenerMetadatos(imagen1);
        String metadatos2 = obtenerMetadatos(imagen2);

        // Concatenar metadatos
        String metadatosConcatenados = metadatos1 + metadatos2;

        // Encriptar metadatos
        String contraseñaSegura = encriptarSHA256(metadatosConcatenados);
        
        
        int maxLongitud = Preferencias.getLongitudMaxima();
        if (contraseñaSegura.length() > maxLongitud) {
            contraseñaSegura = contraseñaSegura.substring(0, maxLongitud);
        }
        // Mostrar contraseña en el JLabel
        Lresult.setText(contraseñaSegura);
    } catch (Exception ex) {
        ex.printStackTrace();
    }
    }//GEN-LAST:event_BgenerarActionPerformed

