    private void BgenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BgenerarActionPerformed
        // Generar cadena
        String cadena1 = TFcadena1.getText();
        String cadena2 = TFcadena2.getText();

        if (cadena1.isEmpty() || cadena2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, complete ambos campos.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            String resultado = PasswordGenerator.generateSecurePassword(cadena1, cadena2);
            Lresult.setText("Resultado: " + resultado);

            // Copiar al portapapeles
            Toolkit.getDefaultToolkit().getSystemClipboard().setContents(new StringSelection(resultado), null);
            JOptionPane.showMessageDialog(this, "Resultado copiado al portapapeles.", "Copiado", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_BgenerarActionPerformed

